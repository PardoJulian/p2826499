package co.edu.sena;

public class Data {
        public Double areaTriangulo(double num1, double num2){
            return (num1 * num2) / 2;}
        public Double sumaNumeros(double num3, double num4){
                return (num3 + num4);}
        public Double numeroElevado(double num5){
                return Math.pow(num5, 2);}
        public Double EuroDolar(double num6){
                return (num6*1.09);}
        public Double AreaCuadrado(double num7){
                return (num7*num7);}
        public Double PerimetroCuadrado(double num7){
                return (4*num7);}
        public Double AreaCilindro(double num8){
                return (Math.PI * Math.pow(num8, 2));}
        public Double VolumenCilindro(double num8, double num9){
                return (Math.PI * Math.pow(num8, 2) * num9);}
        public Double LongitudCircunferencia(double num10){
                return (2 * Math.PI * num10);}
        public Double AreaCircunferencia(double num10){
                return (Math.PI * Math.pow(num10, 2));}
        public Double PromedioNumeros(double num11, double num12, double num13){
                return ((num11+num12+num13)/3);}
        }
        
